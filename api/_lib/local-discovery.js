function safeArray(value) {
  return Array.isArray(value) ? value : [];
}

function evidenceText(payload) {
  return String(payload.extractedText || '');
}

function isMetadataOnlyDiscovery(payload) {
  const text = evidenceText(payload).toLowerCase();
  return (
    text.length < 1200 ||
    text.includes('access binary selected') ||
    text.includes('browser security blocks direct accdb introspection') ||
    text.includes('binary source selected') ||
    text.includes('metadata only') ||
    text.includes('access binary metadata')
  );
}

function sourceType(sourceKind) {
  if (sourceKind === 'access') {
    return 'database';
  }
  if (sourceKind === 'excel') {
    return 'workbook';
  }
  if (sourceKind === 'word') {
    return 'document';
  }
  return 'file';
}

function artifactNames(targetOutputs) {
  const defaults = [
    '01_Executive_Decision_Brief.pdf',
    '02_Current_State_Architecture_Report.pdf',
    '03_Technical_Discovery_Workbook.xlsx',
    '04_Auto_Documentation_Pack',
    '05_Diagram_Pack',
    '07_Action_Backlog.csv',
    '09_Metadata_Manifest.json'
  ];
  return safeArray(targetOutputs).length ? safeArray(targetOutputs) : defaults;
}

function buildLocalDiscovery(payload, reason = 'metadata-only source') {
  const sourceName = payload.sourceName || 'Source batch';
  const sourceKind = payload.sourceKind || 'mixed';
  const knownArtifacts = safeArray(payload.knownArtifacts);
  const targetOutputs = artifactNames(payload.targetOutputs);
  const sourceId = 'SRC-001';
  const blockerId = 'BLOCK-001';
  const outputId = 'OUT-001';
  const evidenceId = 'EV-SRC-001';
  const actionId = 'ACT-001';

  const sourceItem = {
    id: sourceId,
    type: sourceType(sourceKind),
    name: sourceName,
    businessPurpose: 'Staged source selected for discovery. Browser extraction produced metadata and blocker evidence that must be expanded with native exports or connector extraction.',
    owner: 'Source owner',
    evidence: [{
      id: evidenceId,
      type: 'Profile',
      location: knownArtifacts[0] || sourceName,
      description: 'Browser-side file metadata, source name, extension, size, extraction status, and selected target outputs.'
    }],
    confidence: 72,
    criticality: 'medium',
    upstream: [],
    downstream: [blockerId, outputId],
    failureImpact: 'Without native object exports or connector metadata, deep table/query/form/macro lineage cannot be proven from the browser-selected binary alone.',
    dollarExposure: {
      low: 0,
      base: 0,
      high: 0,
      assumptions: 'Dollar exposure is not estimated from binary metadata alone. Add business volume, SLA, labor, or control-impact evidence to price scenarios.'
    },
    recommendedAction: {
      mode: 'stabilize',
      summary: 'Export native source inventory and logic so the discovery graph can trace real objects, transformations, controls, and downstream outputs.',
      owner: 'Data owner',
      priority: 'P0',
      acceptanceCriteria: 'Object inventory, saved SQL/queries, macros/VBA or equivalent connector metadata are staged and linked to this source.'
    },
    status: 'blocked',
    tags: [sourceKind, 'metadata-only', 'needs-native-extract']
  };

  const blockerItem = {
    id: blockerId,
    type: 'control',
    name: 'Native extraction blocker',
    businessPurpose: 'Documents the exact reason deep source intelligence is blocked and the smallest evidence package needed to finish discovery.',
    owner: 'Discovery lead',
    evidence: [{
      id: 'EV-BLOCK-001',
      type: 'Profile',
      location: knownArtifacts[0] || sourceName,
      description: reason
    }],
    confidence: 90,
    criticality: 'high',
    upstream: [sourceId],
    downstream: [outputId],
    failureImpact: 'Engineers cannot rebuild trustworthy Fivetran, dbt, SQL, or Snowpark migration logic until object-level metadata and transformation evidence are available.',
    dollarExposure: {
      low: 0,
      base: 0,
      high: 0,
      assumptions: 'Impact pricing is deferred until critical outputs and business volumes are supplied.'
    },
    recommendedAction: {
      mode: 'govern',
      summary: 'Collect exported Access object inventory, linked table list, saved SQL, relationships, macros, forms/reports, and VBA modules.',
      owner: 'Source owner',
      priority: 'P0',
      acceptanceCriteria: 'Every exported object has an evidence ID, owner, confidence, criticality, upstream/downstream relationship, and next action.'
    },
    status: 'blocked',
    tags: ['blocker', 'evidence-gap', 'access']
  };

  const outputItem = {
    id: outputId,
    type: 'output',
    name: 'Discovery Action Pack',
    businessPurpose: 'Bottled package of current-state documentation, lineage, diagrams, impact assumptions, and engineering actions generated from the canonical model.',
    owner: 'Migration team',
    evidence: [{
      id: 'EV-PACK-001',
      type: 'Profile',
      location: "Generated by Uncle Kev's Distillery",
      description: 'Artifact package generated from the canonical model and blocker-backed source evidence.'
    }],
    confidence: 68,
    criticality: 'high',
    upstream: [sourceId, blockerId],
    downstream: [],
    failureImpact: 'If the blocker remains unresolved, reports and diagrams are useful for planning but cannot be treated as complete lineage proof.',
    dollarExposure: {
      low: 0,
      base: 0,
      high: 0,
      assumptions: 'No monetary exposure was inferred from the provided source metadata.'
    },
    recommendedAction: {
      mode: 'migrate',
      summary: 'Use this blocker-backed pack to drive the next evidence collection pass, then rerun the still with exported metadata.',
      owner: 'Data platform',
      priority: 'P1',
      acceptanceCriteria: 'Second run replaces blocker status with confirmed lineage nodes, edges, controls, transform rules, and backlog actions.'
    },
    status: 'partial',
    tags: ['action-pack', 'lineage', 'reports']
  };

  return {
    processName: sourceName,
    businessFunction: 'Data discovery and migration readiness',
    recommendation: "Complete native source extraction, then rerun Uncle Kev's Distillery to produce confirmed lineage, documentation, diagrams, and engineering actions.",
    decisionRequired: 'Approve access to object-level exports or connector metadata for deep discovery.',
    systemsInScope: knownArtifacts.length ? knownArtifacts : [sourceName],
    criticalOutputs: targetOutputs,
    overallRiskRating: 'high',
    estimatedDollarExposure: {
      low: 0,
      base: 0,
      high: 0,
      assumptions: 'Insufficient business-volume evidence supplied for monetary pricing.'
    },
    items: [sourceItem, blockerItem, outputItem],
    relationships: [
      {
        id: 'REL-001',
        fromId: sourceId,
        toId: blockerId,
        type: 'documented_by',
        automated: false,
        cadence: 'on discovery run',
        confidence: 90,
        evidenceId
      },
      {
        id: 'REL-002',
        fromId: sourceId,
        toId: outputId,
        type: 'depends_on',
        automated: false,
        cadence: 'on discovery run',
        confidence: 72,
        evidenceId
      }
    ],
    artifacts: targetOutputs.map((name, index) => ({
      id: String(index + 1).padStart(2, '0'),
      name,
      type: name.includes('.') ? name.split('.').pop() : 'folder',
      audience: index === 0 ? 'Leadership' : 'Delivery team',
      purpose: 'Generated from the canonical discovery model with blocker-backed evidence.',
      status: 'draft',
      progress: 55
    })),
    failureRisks: [{
      id: 'FAIL-001',
      scenario: 'Source remains metadata-only',
      impactedOutput: 'Recursive lineage and transformation catalog',
      detection: 'Native object exports missing from evidence payload',
      recovery: 'Collect source-native exports and rerun synthesis',
      exposure: {
        low: 0,
        base: 0,
        high: 0,
        assumptions: 'Pricing blocked by missing business-impact inputs.'
      },
      confidence: 90
    }],
    backlog: [{
      actionId,
      title: 'Collect native source exports for deep discovery',
      owner: 'Source owner',
      priority: 'P0',
      dependency: 'Access to source application or exported metadata',
      dueDate: '',
      acceptanceCriteria: 'Tables, queries, relationships, forms/reports, macros, VBA, linked sources, row counts, owners, and critical outputs are provided as readable exports.',
      linkedItemId: blockerId,
      mode: 'govern'
    }],
    openQuestions: [{
      questionId: 'Q-001',
      question: 'Who owns the source and can provide object-level exports or connector metadata?',
      owner: 'Discovery lead',
      dueBy: '',
      impactIfUnanswered: 'Lineage remains blocked and engineering actions stay provisional.'
    }]
  };
}

function localSynthesis(payload, reason) {
  return {
    model: 'local-distillery',
    outputText: JSON.stringify(buildLocalDiscovery(payload, reason)),
    canonicalDelta: buildLocalDiscovery(payload, reason),
    fallbackReason: reason
  };
}

module.exports = {
  buildLocalDiscovery,
  isMetadataOnlyDiscovery,
  localSynthesis
};
